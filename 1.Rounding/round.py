# Problem 1:
# You want to round a floting point number to a fixed number of decimal places

# For simple rounding just do round(value, ndigits)

# Simple float
float_num = 13.42575

print(round(float_num, 1))
print(round(float_num, 2))
print(round(float_num, 3))

# Round to decimal
a = round(float_num, 0)
print(a)

